// Addition: '+'
// Subtraction: '-'
// MUltiplication: '*'
// Division: '/'
// Modulus: '%'
// Increment: '++'
// Decrement: '--'

console.clear()

var num1 = 10;
var num2 = 15;
var num3 = 20;

var num4 = 30;

//Addition

console.log(num1 + num2 + num3)

// Subtraction

console.log(num1 + num2 - num3)

// Multiplication

console.log(num1 * num2 * num3)

// Division

console.log(num1 / num2 / num3)

// Modulus

console.log(num1 % num2 % num3)

// Increment

console.log(num1++)
console.log(num1)

console.log(++num2)


// Decrement

console.log(num3--)
console.log(num3)
console.log(--num4)